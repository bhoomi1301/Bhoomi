package com.hashSetDemo;


import java.util.HashSet;

public class HashSetDemo2 {

	public static void main(String[] args) {
		HashSet<String> h1 = new HashSet<String>();
		//unordered  and unsorted
		h1.add("bhoomi");
		h1.add("abhi");
		h1.add("shobha");
		h1.add("satya");
		System.out.println(h1);

	}

}
